import re

def extract_id_codes(text):
    """
    Вилучає ID-коди жителів з тексту.
    ID-код має формат: дві літери, цифра, літера, дефіс, п'ять цифр, 
    дефіс, дві літери.
    Наприклад: NT3K-78901-XZ
    """
    pattern = r'\b[A-Z]{2}\d[A-Z]-\d{5}-[A-Z]{2}\b'
    return re.findall(pattern, text)

# Приклад використання
futuristic_text = """
Вітаємо вас у NeoTerra 3000, де технології та природа зливаються воєдино! 
Наше місто, засноване в 2150 році, є домом для 5 мільйонів жителів та 
незліченної кількості ШІ-помічників.
Кожен житель NeoTerra має свій унікальний ID-код, наприклад NT3K-78901-XZ, 
який використовується для доступу до всіх міських сервісів. Наші квантові 
комп'ютери, такі як QC-9000, обробляють петабайти даних щосекунди, 
забезпечуючи безперебійну роботу міста.
… 
"""

# Вилучення ID-кодів
id_codes = extract_id_codes(futuristic_text)

# Виведення результатів
print("Знайдені ID-коди жителів:")
for code in id_codes:
    print(code)
